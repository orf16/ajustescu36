function ValidarVigencia(fecha){
	var vigencia = formatFecha($("#"+fecha+"").val());
	var hoy             = new Date();
	var fechaFormulario = new Date(vigencia);
	hoy.setHours(0,0,0,0);
	
	if (fechaFormulario >= hoy) {
		swal("Estimado Usuario", "La fecha seleccionada no puede ser mayor a la actual.", "warning");
		$("#"+fecha+"").val("")
		$("#"+fecha+"").focus();
	}

}

function formatFecha(fecha) {
    if (fecha == null)
    {
        return "";
    }
	var fec = fecha.split("/");
	return fec[2]+"-"+fec[1]+"-"+fec[0];
}

