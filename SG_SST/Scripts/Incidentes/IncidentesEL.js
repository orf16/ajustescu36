function CargarEmpleado(numeroDocumento)
{
	$.ajax({
		type: 'POST',
		url: '/IncidentesEL/ConsultarDatosTrabajador',
		data: { numeroDocumento : numeroDocumento },
		success: function (result) {
			if(result.Mensaje!="OK")
				swal("Estimado Usuario",result.Data, "warning");
			else{

				if(result.Data.tipoDoc == "CC"){
					$("#TipoIdentificacionIII3").prop('checked',true);
					$("#TipoIdentificacionIII4").prop('disabled',true);
					$("#TipoIdentificacionIII5").prop('disabled',true);
					$("#TipoIdentificacionIII").prop('disabled',true);
				}
				
				if(result.Data.tipoDoc == "CE"){
					$("#TipoIdentificacionIII4").prop('checked',true);
					$("#TipoIdentificacionIII3").prop('disabled',true);
					$("#TipoIdentificacionIII5").prop('disabled',true);
					$("#TipoIdentificacionIII").prop('disabled',true);
				}
				
				if(result.Data.tipoDoc == "NU"){
					$("#TipoIdentificacionIII5").prop('checked',true);
					$("#TipoIdentificacionIII4").prop('disabled',true);
					$("#TipoIdentificacionIII3").prop('disabled',true);
					$("#TipoIdentificacionIII").prop('disabled',true);
				}
				
				if(result.Data.tipoDoc == "PA"){
					$("#TipoIdentificacionIII").prop('checked',true);
					$("#TipoIdentificacionIII5").prop('disabled',true);
					$("#TipoIdentificacionIII4").prop('disabled',true);
					$("#TipoIdentificacionIII3").prop('disabled',true);
				}

				$("#PrimerApellidoIII").val(result.Data.apellido1);
				$("#SegundoApellidoIII").val(result.Data.apellido2);
				$("#PrimerNombreIII").val(result.Data.nombre1);
				$("#SegundoNombreIII").val(result.Data.nombre2);
				$("#FechaNacimientoIII").val(result.Data.fechaNacimiento);
				
				if(result.Data.sexoPersona=="F")
					$("#SexoIII").val("F");
				else
					$("#SexoIII").val("M");
				

				$("#EPSAfiliadoIII").val(result.Data.nombreEps);
				$("#AFPAfiliadoIII").val(result.Data.nombreAfp);
				$("#ARLAfiliadoIII").val(result.Data.nombreArl);
				$("#TelefonoIII").val(result.Data.telPersona);
				$("#FaxIII").val(result.Data.faxEmpresa);
				$("#EmailTrabajadorIII").val(result.Data.emailPersona);
				$("#DireccionTrabajadorIII").val(result.Data.dirEmpresa);
				
				$("#CodigoDeptoTrabajadorIII").val(result.Data.idDepAfiliado);
				$("#DeptoTrabajadorIII").val(result.Data.nomDepAfiliado);
				$("#CodigoMncpioTrabajadorIII").val(result.Data.idMunAfiliado);
				$("#MncpioTrabajadorIII").val(result.Data.nomMunAfiliado);

				if(result.Data.idZona=="U")
					$("#ZonaIII2").prop('checked',true);
				else
					$("#ZonaIII").prop('checked',true);
				

			}	
		}
	});
}

